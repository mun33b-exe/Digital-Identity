    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        // TODO add your handling code here:
        String r_name=name.getText();
        String r_father=father_name.getText();
        if("".equals(r_name) || "".equals(r_father)){
            JOptionPane.showMessageDialog(  null, "First enter name and father name");
        }
        else{
            try {
                Conn c=new Conn();
                String query= "select * from refugee where Name='"+r_name+"' and Father_Name='"+r_father+"'";
                ResultSet rs=c.s.executeQuery(query);
                if (rs.next()) {
                            new delete_module(action).setVisible(true);
                            this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Refugee Not Found");
                
            }                
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(  null, "Database Connection Problem"); 
            }
        }
    }//GEN-LAST:event_jButton4MouseClicked

