    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
// TODO add your handling code here:
              String r_id=R_ID.getText();
        if("".equals(r_id) ){
            JOptionPane.showMessageDialog(  null, "First enter ID");
        }
        else{
            try {
                Conn c=new Conn();
                String query= "select * from refugee where Ref_ID='"+r_id+"'";
                ResultSet rs=c.s.executeQuery(query);
                if (rs.next()) {
                            new delete_module(action).setVisible(true);
                            this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Refugee Not Found");
                
            }                
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(  null, "Database Connection Problem"); 
            }
        }
    }//GEN-LAST:event_jButton7MouseClicked

